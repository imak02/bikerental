function functionhide() {
    var obj = document.getElementById('formhiding');
    obj.style.visibility = "hidden";
}

function functionshow1() {
    var obj = document.getElementById('formhiding');
    obj.style.visibility = "visible";

    var VehId = document.getElementById('Veh1').value;
    document.getElementById('VehicleId').value = VehId;

    var Price = document.getElementById('veh1price').value;
    document.getElementById('ttlprice').value = Price;
}

